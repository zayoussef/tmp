#include "libft.h"

int	ft_isspace(int c)
{
	return (c == ' ' || (c >= 9 && c <= 13));
}

int	ft_isdigit(int c)
{
	return (c >= '0' && c <= '9');
}

int	ft_isalpha(char c)
{
	return ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'));
}

int	ft_isalnum(int c)
{
	return ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z') || (c >= '0'
			&& c <= '9'));
}

long long	ft_atoll(char *s)
{
	long long	rs;
	int			sign;

	rs = 0;
	sign = 1;
	while (ft_isspace(*s))
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			sign = -1;
		s++;
	}
	while (*s >= '0' && *s <= '9')
	{
		if ((rs > LLONG_MAX / 10) || (rs == LLONG_MAX / 10 && *s
				- '0' > LLONG_MAX % 10))
			return (LLONG_MIN);
		rs = rs * 10 + (*s - '0');
		s++;
	}
	return (rs * sign);
}
